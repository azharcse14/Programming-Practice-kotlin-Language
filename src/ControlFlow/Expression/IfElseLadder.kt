package Kotlin.ControlFlow.Expression

fun main() {
    var designation = "ISE"
    if (designation == "SSE") {
        println("Provide PC")
    }else if (designation == "SE") {
        println("Provide PC")
    }else if (designation == "JSE") {
        println("Provide PC")
    }else if (designation == "ISE") {
        println("Provide PC")
    }
    else{
        println("Provide Tablet")
    }
}